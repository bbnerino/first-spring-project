package com.example.pracspring.domain;

public class Member { // 모든 계층에서 사용하기 위한 POJO
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
